nome = "Efraim Rocha da Silva"

#conta o primeiro como 0
print (nome [0])

#retornará caracteres em ordem inversa do que foi passado
print (nome [-2])

#não conta o primeiro como 0
print (nome [:6])

#não ignora espaços
print (nome [6:])

#quando não informa início, parada e passo, mostra toda sequência
print (nome [:])

#limita o início e o fim
print (nome [6:19])

            #início, fim, passo
print (nome [6:19:2])

#espelha os caracteres
print (nome [::-1])