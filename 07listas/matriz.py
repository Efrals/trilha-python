matriz = [
    [0, 1, 2],
    ["a", "b", "c"],
    ["macã", "uva", "cebola"]
]

print(f"O que está na linha [0] é: {matriz[0]}")
print(f"O que está na linha [1] é: {matriz[1]}")
print(f"O que está na linha [2] é: {matriz[2]}")
print(f"O que está em matriz[0][0] é: {matriz[0][0]}")
print(f"O que está em matriz[2][2] é: {matriz[2][2]}")
print(f"O que está em matriz[2][0] é: {matriz[2][0]}")
print(f"O que está em matriz[2][-1] é: {matriz[2][-1]}")
print(f"O que está em matriz[-1][-1] é: {matriz[-1][-1]}")