saldo = 10

#atribuição com adição
saldo += 2
print(saldo)

#atribuição com subtração
saldo -= 2
print(saldo)

#atribuição com multipliação
saldo *= 2
print(saldo)

#atribuição com divisão
saldo /= 2
print(saldo)

#atribuição com divisão sem float
saldo //= 2
print(saldo)

#atribuição com módulo
saldo %= 2
print(saldo)

#atribuição com exponenciação
saldo **= 2
print(saldo)